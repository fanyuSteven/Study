package qmf.operator;

public class Demo03 {
    public static void main(String[] args) {
        //关系运算符返回的结果：true false 布尔值
        int a=10;
        int b=20;

        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a==b);
        System.out.println(a!=b);
    }
}
